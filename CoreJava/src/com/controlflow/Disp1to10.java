public class Disp1to10 {
	public static void main(String[] args)
	{
		int i=1;
		while(i<=10)
		{
			System.out.print(i+" ");
			i++;
		}
		System.out.println();
		
		System.out.println("*****REVERSE LOOP*****");
		int j=10;
		while(j>=1)
		{
			System.out.print(j+" ");
			j--;
		}
	}

}
